<?php
// + (tambah) Operator aritmatika
$x = 10;  
$y = 6;

echo $x + $y;

// - (kurang) Operator aritmatika
$x = 10;  
$y = 6;

echo $x - $y;

// * (kali) Operator aritmatika
$x = 10;  
$y = 6;

echo $x * $y;

// / (bagi) Operator aritmatika
$x = 10;  
$y = 6;

echo $x / $y;

// % (modulus) Operator aritmatika
$x = 10;  
$y = 6;

echo $x % $y;

// ** (pangkat) Operator aritmatika
$x = 10;  
$y = 3;

echo $x ** $y;
